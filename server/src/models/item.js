import mongoose from "mongoose";
import UserSchema from "./user.js";

const ItemSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  userId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true,
  },
});

const model = mongoose.model("Item", ItemSchema);

export default model;